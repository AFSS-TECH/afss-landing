// Auto-generates src/blog-meta.js (lightweight post metadata, no article body)
// from src/blog.js. Runs before each build (see package.json "build") so the
// homepage teaser / blog index never bundle the full ~600KB of article markdown.
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { getAllPosts } from '../src/blog.js'

const __dirname = dirname(fileURLToPath(import.meta.url))
const out = join(__dirname, '..', 'src', 'blog-meta.js')

const meta = getAllPosts().map(({ body, ...rest }) => rest)

const content = `// AUTO-GENERATED by scripts/gen-blog-meta.mjs — do not edit by hand.
// Lightweight blog metadata (no article body) for the homepage teaser and
// blog index/search/tags, so pages that don't need full article text don't
// have to bundle src/blog.js (~600KB of markdown).
export const postsMeta = ${JSON.stringify(meta, null, 2)}
`

writeFileSync(out, content)
console.log(`[gen-blog-meta] wrote ${meta.length} posts to src/blog-meta.js`)
